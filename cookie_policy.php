What are cookies?

Cookies are small text files that are stored on your computer or mobile device when you visit a website. They are used to help the website remember your preferences and settings, and to provide personalized features and content. Cookies can be set by the website you are visiting (first-party cookies) or by other websites that provide services to that website (third-party cookies).

How do we use cookies?

We use cookies to improve the functionality of our site, to personalize your experience, and to provide targeted advertising. For example, we may use cookies to:

Remember your login details and preferences
Track your use of our site and services
Serve you with personalized ads based on your interests
Analyze the performance of our site and services
We also use cookies to help protect your account and our site from unauthorized access.

Third-party cookies

We may also use third-party cookies from service providers that help us deliver our services and provide additional features on our site. These cookies may collect information about your use of our site, as well as your IP address and other personal data. We do not have access to or control over these cookies, and we encourage you to review the privacy policies of these third-party service providers.

Managing your cookie preferences

You can manage your cookie preferences in your browser settings. Most browsers allow you to block or delete cookies, but this may affect the functionality of our site and other websites that you visit. Consult your browser's help documentation for more information.

